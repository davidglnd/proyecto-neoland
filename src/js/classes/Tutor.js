class Tutor {
/**
 * Constructor de la clase Tutor
 * @param {string} name - El nombre del tutor
 * @param {string} apellido1 - El primer apellido del tutor
 * @param {string} apellido2 - El segundo apellido del tutor
 * @param {string} email - El email del tutor
 * @param {string} telfono - El telfono del tutor
 */
    constructor(name, apellido1, apellido2, email, telfono) {
      this.name = name
      this.apellido1 = apellido1
      this.apellido2 = apellido2
      this.email = email
      this.telfono = telfono 
    }
}